    private void boton_BuscarPersonaVacunadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_BuscarPersonaVacunadaActionPerformed
        Verificador veri = new Verificador();
        if (veri.dataNB(textField_NDocumento)){
            if (textField_NDocumento.getText().toString().matches("-?\\d+")){
                bus.limpiarTabla(jTable_PersonaVacunada);
                bus.tablePersonaVacunada(jTable_PersonaVacunada, desplegable_Nacionalidad.getSelectedItem().toString()+textField_NDocumento.getText().toString());
            }else
                JOptionPane.showMessageDialog(null, "La cedula debe ser numeros enteros","Error",ERROR_MESSAGE);
        }else{
            bus.limpiarTabla(jTable_PersonaVacunada);
            bus.tableAllPersonaVacunada(jTable_PersonaVacunada);
        }
    }//GEN-LAST:event_boton_BuscarPersonaVacunadaActionPerformed

