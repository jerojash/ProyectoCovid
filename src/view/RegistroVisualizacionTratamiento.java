    private void boton_AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_AgregarActionPerformed
    Verificador veri = new Verificador();
        model = (DefaultTableModel) TableMedicamentos.getModel();
        String Dosis; 
        String CantDias; 
        String Frecuencia;
        String nombmedicamento = desplegable_Medicamentos.getSelectedItem().toString();
        //dato2 = model.getValueAt(jTable_tratamientos.getSelectedRow(), 0).toString();
        String codtrat = field_codtrat.getText().toString();
        System.out.println(codtrat);
        if (veri.dataNB(field_Dosis)) {
            if (field_Dosis.getText().toString().matches("-?\\d+")){
                if (veri.dataNB(field_codtrat)) {
                    if (field_codtrat.getText().toString().matches("-?\\d+")){
                        if (veri.dataNB(field_Frecuencia)) {
                            if (field_Frecuencia.getText().toString().matches("-?\\d+")){
                                Dosis = field_Dosis.getText().toString();
                                CantDias = field_CantDias1.getText().toString();
                                Frecuencia = field_Frecuencia.getText().toString();
                                if(!veri.existenciaTable(model, nombmedicamento)){
                                    model.addRow(new Object[]{nombmedicamento, Dosis, CantDias, Frecuencia});   
                                    Guardar insertSQL = new Guardar();
                                    
                                    insertSQL.guardarTratMed(codtrat, nombmedicamento, Dosis, CantDias, Frecuencia);
                                    JOptionPane.showMessageDialog(null, "Operación realizada exitosamente");
                                    System.out.println("voy por la persistencia");
                                    field_Dosis.setText("");
                                    field_CantDias1.setText("");
                                    field_Frecuencia.setText(""); 
                                    

                                }
                            }else
                                  JOptionPane.showMessageDialog(null, "La frecuencia debe ser un valor entero","Error",ERROR_MESSAGE);
                        }  
                 }else
                     JOptionPane.showMessageDialog(null, "La cantidad de dias debe ser un valor entero","Error",ERROR_MESSAGE);
                }   
            }else
                JOptionPane.showMessageDialog(null, "La dosis debe ser un valor entero","Error",ERROR_MESSAGE);
        }
    }//GEN-LAST:event_boton_AgregarActionPerformed

