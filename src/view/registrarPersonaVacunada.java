    private void boton_Siguiente_RegistroDPVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_Siguiente_RegistroDPVActionPerformed
        Verificador veri = new Verificador();
        if(veri.dataNB(textField_NDocumento)){
            if((veri.noVacio(textField_NDocumento.getText().toString()))){
                if(!(desplegable_Nacionalidad.getSelectedItem().toString()+textField_NDocumento.getText().toString()).equals(desplegable_PersonalSalud.getSelectedItem().toString())){
                    Guardar insertSQL = new Guardar();
                    Buscador bus = new Buscador();
                    String cedula = desplegable_Nacionalidad.getSelectedItem().toString()+textField_NDocumento.getText().toString();
                    String vacuna = bus.codVacuna(desplegable_VacApli1.getSelectedItem().toString());
                    ArrayList<String> cadena = new ArrayList<String>();
                    cadena = bus.arrayDosis(cedula, vacuna);
                    if(insertSQL.vacunacion(cedula,vacuna,bus.codCentro(desplegable_CentroApli.getSelectedItem().toString()),desplegable_PersonalSalud.getSelectedItem().toString(),desplegable_calendar,String.valueOf(cadena.size()+1))){
                        JOptionPane.showMessageDialog(null, "La dosis aplicada fue la: "+String.valueOf(cadena.size()+1));
                        RegistroVisualizacionPersonasVacunadas persona= new RegistroVisualizacionPersonasVacunadas();
                        crea.InterfazDiferentes(this, persona, 837, 581);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "La persona a vacunar no debe coincidir con el personal a vacunar","Error",ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "No se admiten casillas con unicamente espacios en blanco","Error",ERROR_MESSAGE);
            }
        }else
            JOptionPane.showMessageDialog(null, "Se encuentran casillas sin rellenar","Error",ERROR_MESSAGE);
    }//GEN-LAST:event_boton_Siguiente_RegistroDPVActionPerformed

